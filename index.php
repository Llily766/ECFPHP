      <h4>  Arcadia est située en France,près de la forêt de brocéliande ,en bretagne, depuis 1960.
    Ils possèdent tout un panel d'animaux et essaye de reconstituer à l'identique leurs habitats :
    savane,
    marais,
    jungle,
    Les animaux sont inspectés chaque jour par un vétérinaire.

    D un point de vue financier ,le zoo se porte bien ,cela fait plaisir à José,directeur de ce zoo.
    A ce jour, José n'a pas de site web pour son zoo ,par conséquent il a mandaté Josette pour
    trouver une entreprise ,qui va lui permettre de créer son site web.
    Après l'obtention du diplome , j'ai été mandaté au sein de la société DEVSoft
    pour réaliser leur site web.
    Précision importante de José:“Je souhaite avoir une
    application web dont les couleurs et le thème font penser à de l’écologie, le zoo est
    entièrement indépendant au niveau des énergies et il faut que lorsqu’un utilisateur consulte
    l’application, il ressente les valeurs de l’écologie dont nous sommes fiere”</h4>

